#include <stdio.h>

int main(){
    //  Declaring variables
    //  syntax: datatype variablename;
    //  eg: int a;
    int age;
    age = 18;


    //  Declaration and initalization of variable at a time
    //  syntax: datatype varname = value;
    float pi = 3.14;
    char grade = 'a';
    double code = 1535;

    //  Printing Variable Values
    printf("Age: %d\n", age);
    printf("Pi: %f\n", pi);
    printf("Grade: %c\n", grade);
    printf("Code: %lf\n", code);

    return 0;
}
